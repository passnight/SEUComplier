int yylex() {
    int state = 85;
    int nextChar;
    yytext = "";
    yylength = 0;
    int lastAccept = -1;
    unsigned int lastLength = 0;

    while (true) {
        if (stdin == yyin)
            nextChar = cin.get();
        else
            nextChar = fgetc(yyin);
        if (nextChar == EOF)
            if (0 == yylength)
                return -1;
            else
                state = -1;
        else
        {
            state = transitionTable[state][nextChar];
            yytext.push_back(nextChar);
            yylength++;
            if (actionMap.find(state) != actionMap.end()) {
                lastAccept = state;
                lastLength = yylength;
            }
        }
        if (state == -1) {
            while (yylength > lastLength + (lastAccept == -1)) {
                if (stdin == yyin)
                    cin.unget();
                else
                    fseek(yyin, -1, SEEK_CUR);
                yytext.pop_back();
                yylength--;
            }
            if (lastAccept == -1)
                return acceptDefault();
            else
                return actionMap.at(lastAccept)();
        }
    }
    return 0;
}


